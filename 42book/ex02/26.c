#include <stdio.h>

int	main(void)
{
	const int TAX = 110;
	int fax = 50000;
	printf("50000円から40000円に値下げしました。\n");
	TAX = 40000;
	printf("FAXの新価格(税込) : %d円\n", fax * TAX / 100);
	return (0);
}
